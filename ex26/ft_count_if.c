int	ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int mas;

	i = 0;
	mas = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) == 1)
			mas++;
		i++;
	}
	return (mas);
}
